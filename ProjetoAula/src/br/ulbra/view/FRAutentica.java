    private void logar() {
        UsuarioController controller = new UsuarioController();
        char[] senha = txtSenha.getPassword();
        
        if(controller.autenticar(txtEmail.getText(), new String (senha))) {
            this.dispose();
            new FRMenu().setVisible(true);
        }
    }
