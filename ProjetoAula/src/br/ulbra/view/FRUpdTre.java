    private void btExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btExcluirMouseClicked
        int resposta = JOptionPane.showConfirmDialog(null, "Deseja excluir o treino?",
            "Confirmação", JOptionPane.YES_NO_OPTION);

        if (resposta == JOptionPane.YES_OPTION) {
            TreinoController controller = new TreinoController();
            if(controller.excluirTreino(treino_pk)){
                this.dispose();
            }
        }
    }//GEN-LAST:event_btExcluirMouseClicked

    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        TreinoController controller = new TreinoController();
        Treino usu = controller.readForPk(treino_pk);
       
        String codigo = String.valueOf(usu.getTreino_pk());
        txtCodigo.setText(codigo);
        txtNome.setText(usu.getNomeTre());
        txtDescricao.setText(usu.getDescricaoTre());
        txtDtTreino.setText(usu.getDataTre());
        AtivoUsu.setSelected(usu.getAtivoTre() == 1);
    }//GEN-LAST:event_formWindowActivated

