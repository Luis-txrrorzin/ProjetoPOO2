    private void btAlterarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btAlterarMouseClicked
       if (!verificarCampos()) {
        return;
    }

    TreinoController controller = new TreinoController();
    Treino treino = new Treino();
    treino.setTreino_pk(treino_pk);
    treino.setNomeTre(txtNome.getText());
    treino.setDescricaoTre(txtDescricao.getText());
    treino.setDataTre(txtDtTreino.getText());
    treino.setAtivoTre(Utils.salvarBoolean(AtivoUsu.isSelected()));
    if (controller.alterarTreino(treino)) {
        this.dispose();
    }
    }//GEN-LAST:event_btAlterarMouseClicked

